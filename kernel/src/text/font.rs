use core::ascii::Char;

use bitvec::array::BitArray;

/// Font trait
pub trait Font<const W: usize, const H: usize> {
    /// Width in pixels
    fn width() -> usize { W }
    /// Height in pixels
    fn height() -> usize { H }
    /// Get bitmap for character, fg = 1, bg = 0
    fn get_char(c: Char) -> BitArray<[u8; (W * H + 7) / 8]>;
}

